        shift 1
        add $@
        ;;
    del)
        shift 1
        [ -z $1 ] && echo "Usage: del xx:xx:xx:xx:xx:xx [athxx] " && exit 1
        del $@
        ;;
    *)
        echo "Usage: $0 [restart|start|stop|add|del|show]"
        echo "--->param: $*"
        lock -u /var/run/qos.lock
        exit 1
        ;;
esac

